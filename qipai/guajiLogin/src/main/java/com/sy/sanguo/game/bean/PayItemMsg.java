package com.sy.sanguo.game.bean;

public class PayItemMsg {
	private int v;
	private int cny;
	private String n;
	private int send;

	public int getV() {
		return v;
	}

	public void setV(int v) {
		this.v = v;
	}

	public int getCny() {
		return cny;
	}

	public void setCny(int cny) {
		this.cny = cny;
	}

	public String getN() {
		return n;
	}

	public void setN(String n) {
		this.n = n;
	}

	public int getSend() {
		return send;
	}

	public void setSend(int send) {
		this.send = send;
	}

}
