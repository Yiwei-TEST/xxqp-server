package com.sy599.game.staticdata.model;

public class Award {
	private int id;
	private int freeCards;
	private int cards;

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public int getFreeCards() {
		return freeCards;
	}

	public void setFreeCards(int freeCards) {
		this.freeCards = freeCards;
	}

	public int getCards() {
		return cards;
	}

	public void setCards(int cards) {
		this.cards = cards;
	}

}
